class Operaciones:
    def suma(a,b):
        return print(a+b)
    
    def resta(a,b):
        return print(a-b)
    
    def multiplicacion(a,b):
        return print(a*b)
    
    def division(a,b):
        return print(a/b)
    
Operaciones.suma(2,3)
Operaciones.resta(2,3)
Operaciones.multiplicacion(2,3)
Operaciones.division(2,3)